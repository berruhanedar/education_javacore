package com.berruhanedar._2_week;

public class _09_2_Conditional {
    public static void main(String[] args) {
        int number = 5;

        // If it has a single statement, we don't have to use curly brackets.
        if (number > 0)
            System.out.println("It's positive number");
        else
            System.out.println("It's negative number");
    }
}
