package com.berruhanedar._2_week;

public class _10_3_Loop_DoWhile {
    public static void main(String[] args) {
        // Regardless of whether the condition is met or not, it will execute at least once.
        int i = 5;
        do {
            System.out.print(i + " - ");
            i++;

        } while (i <= 10);

    }
}
